import { createHash } from "node:crypto";
import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const distDir = path.resolve(process.cwd(), "dist");
const serviceWorkerFileName = "offline-sw.js";
const excludedFileNames = new Set([serviceWorkerFileName]);
const excludedTopLevelDirectories = new Set(["examples"]);

function hasHiddenPathSegment(relativePath) {
  return relativePath.split("/").some((segment) => segment.startsWith("."));
}

function isExcludedFromOfflinePrecache(relativePath) {
  return excludedTopLevelDirectories.has(relativePath.split("/")[0]);
}

async function listDistFiles(directory, prefix = "") {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const relativePath = prefix ? `${prefix}/${entry.name}` : entry.name;
    const absolutePath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      files.push(...(await listDistFiles(absolutePath, relativePath)));
      continue;
    }

    if (
      !entry.isFile() ||
      excludedFileNames.has(relativePath) ||
      hasHiddenPathSegment(relativePath) ||
      isExcludedFromOfflinePrecache(relativePath)
    ) {
      continue;
    }

    files.push(relativePath);
  }

  return files.sort((left, right) => left.localeCompare(right));
}

const files = await listDistFiles(distDir);
const assetManifest = [];
const versionHash = createHash("sha256");

for (const file of files) {
  const bytes = await readFile(path.join(distDir, file));
  const fileHash = createHash("sha256").update(bytes).digest("hex");
  versionHash.update(file);
  versionHash.update(fileHash);
  assetManifest.push(`./${file}`);
}

const precacheUrls = Array.from(new Set(["./", "./index.html", ...assetManifest]));
const cacheVersion = versionHash.digest("hex").slice(0, 16);

const workerSource = `/* Generated by scripts/generate-offline-service-worker.mjs. */
const CACHE_VERSION = ${JSON.stringify(cacheVersion)};
const PRECACHE_NAME = \`dj1000-precache-\${CACHE_VERSION}\`;
const RUNTIME_NAME = \`dj1000-runtime-\${CACHE_VERSION}\`;
const PRECACHE_URLS = ${JSON.stringify(precacheUrls, null, 2)};

function absoluteUrl(url) {
  return new URL(url, self.registration.scope).toString();
}

const PRECACHE_URL_SET = new Set(PRECACHE_URLS.map(absoluteUrl));
const APP_SHELL_URL = absoluteUrl("./index.html");

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches
      .open(PRECACHE_NAME)
      .then((cache) =>
        cache.addAll(
          PRECACHE_URLS.map((url) =>
            new Request(absoluteUrl(url), {
              cache: "reload",
            }),
          ),
        ),
      )
      .then(() => self.skipWaiting()),
  );
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((cacheNames) =>
        Promise.all(
          cacheNames
            .filter((cacheName) => cacheName.startsWith("dj1000-") && cacheName !== PRECACHE_NAME && cacheName !== RUNTIME_NAME)
            .map((cacheName) => caches.delete(cacheName)),
        ),
      )
      .then(() => self.clients.claim()),
  );
});

async function cacheFirst(request) {
  const cached = await caches.match(request);
  if (cached) {
    return cached;
  }

  const response = await fetch(request);
  if (response.ok) {
    const cache = await caches.open(RUNTIME_NAME);
    await cache.put(request, response.clone());
  }
  return response;
}

async function navigationFallback(request) {
  try {
    const response = await fetch(request);
    if (response.ok) {
      const cache = await caches.open(RUNTIME_NAME);
      await cache.put(request, response.clone());
    }
    return response;
  } catch {
    const cached = await caches.match(request);
    return cached ?? caches.match(APP_SHELL_URL);
  }
}

self.addEventListener("fetch", (event) => {
  const { request } = event;
  if (request.method !== "GET") {
    return;
  }

  const requestUrl = new URL(request.url);
  const scopeUrl = new URL(self.registration.scope);
  if (requestUrl.origin !== scopeUrl.origin || !requestUrl.pathname.startsWith(scopeUrl.pathname)) {
    return;
  }

  if (request.mode === "navigate") {
    event.respondWith(navigationFallback(request));
    return;
  }

  if (PRECACHE_URL_SET.has(requestUrl.toString())) {
    event.respondWith(cacheFirst(request));
  }
});
`;

await writeFile(path.join(distDir, serviceWorkerFileName), workerSource);

console.log(`Generated ${serviceWorkerFileName} with ${precacheUrls.length} offline URLs.`);
